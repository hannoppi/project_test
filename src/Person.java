
public class Person {
	String id;
	String password;
	
	public Person (String id, String password) {
		this.id = id;
		this.password = password;
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setName(String password) {
		this.password = password;
	}
}
